function calculateHypotenuse(a, b) {
    return Math.sqrt(a*a + b*b);
  }

  // Beispiel: Seitenlängen a und b
  var a = 3;
  var b = 4;

  // Berechne die Hypotenuse
  var c = calculateHypotenuse(a, b);

  // Gib das Ergebnis aus
  console.log("Seitenlänge a: " + a);
  console.log("Seitenlänge b: " + b);
  console.log("Hypotenuse c: " + c);


